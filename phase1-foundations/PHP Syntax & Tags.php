<?php // Opening PHP tag - starts PHP code

// PHP SYNTAX EXAMPLES:

// 1. Variables (must start with $)
$name = "John";

// 2. Statements end with semicolon ;
echo $name;

// 3. Comments
// This is a comment

// 4. Case sensitivity (variables are case sensitive)
$age = 25; // different from $Age

// 5. Echo output
echo "Hello World";

?> <!-- Optional closing tag - ends PHP code -->

<!-- Back to HTML -->
<p>This is HTML</p>

<?php 
// Starting PHP again
echo "Back to PHP";
// No closing tag at end of file (recommended for pure PHP files)